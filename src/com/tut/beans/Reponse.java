package com.tut.beans;

public class Reponse {
	
	private Integer idReponse;
	private String texteReponse;
	/**
	 * @return the idReponse
	 */
	public Integer getIdReponse() {
		return idReponse;
	}
	/**
	 * @param idReponse the idReponse to set
	 */
	public void setIdReponse(Integer idReponse) {
		this.idReponse = idReponse;
	}
	/**
	 * @return the texteReponse
	 */
	public String getTexteReponse() {
		return texteReponse;
	}
	/**
	 * @param texteReponse the texteReponse to set
	 */
	public void setTexteReponse(String texteReponse) {
		this.texteReponse = texteReponse;
	}
	

}
